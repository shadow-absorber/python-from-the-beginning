Fahrenheit = float(input('Vad 채r temperaturen i Fahrenheit?: '))
Rankine = Fahrenheit + 459.67
Celsius = (Fahrenheit - 32) * 5 / 9
Kelvin = Celsius + 273.15

print(f'Temperaturen i Rankine 채r {Rankine:.2f}')
print(f'Temperaturen i Celsius 채r {Celsius:.2f}')
print(f'Temperaturen i Kelvin 채r {Kelvin:.2f}')
